<?php
require_once dirname(__FILE__) . "/FusionBase.php";

abstract class ValidateBase extends FusionBase
{
	// メンバ変数定義
	private $_params;				// パラメータ
	private $_message;				// チェック結果が偽の場合に出力したいメッセージ

	//--------------------------------------------------------------------------
	// ValidateBase::main (abstract)
	//--------------------------------------------------------------------------
	// 主処理
	// 
	// 
	// 
	// 
	//--------------------------------------------------------------------------
	abstract public function main($value);

	//--------------------------------------------------------------------------
	// ValidateBase::setMessage
	//--------------------------------------------------------------------------
	// メッセージ設定
	// 
	// 
	// 
	// 
	//--------------------------------------------------------------------------
	public function setMessage($value)
	{
		$this->_message		= $value;
	}

	//--------------------------------------------------------------------------
	// ValidateBase::getMessage
	//--------------------------------------------------------------------------
	// メッセージ取得
	// 
	// 
	// 
	// 
	//--------------------------------------------------------------------------
	public function getMessage()
	{
		return ($this->_message);
	}

	//--------------------------------------------------------------------------
	// ValidateBase::addParam
	//--------------------------------------------------------------------------
	// 
	// 
	// 
	// 
	// 
	//--------------------------------------------------------------------------
	public function addParam($name, $value)
	{
		if ($this->_params == null){ $this->_params = array(); }
		$this->_params[$name]		= $value;
	}

	//--------------------------------------------------------------------------
	// ValidateBase::setParams
	//--------------------------------------------------------------------------
	// 
	// 
	// 
	// 
	// 
	//--------------------------------------------------------------------------
	public function setParams($value)
	{
		$this->_params		= $value;
	}

	//--------------------------------------------------------------------------
	// ValidateBase::getParams
	//--------------------------------------------------------------------------
	// 
	// 
	// 
	// 
	// 
	//--------------------------------------------------------------------------
	public function getParams()
	{
		return $this->_params;
	}
}
?>
